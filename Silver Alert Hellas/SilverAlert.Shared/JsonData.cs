        public static List<MissingPerson> MissingPeopleList(string LanguageCode, string json, Category category)
        {
            try
            {
                JArray jsonArray = JArray.Parse(json);

                IList<MissingPerson> TempList = jsonArray.Select(p => new MissingPerson
                {
                    ID = (int)p["id"],
                    Name = (string)p["name"][LanguageCode],
                    Surname = (string)p["surname"][LanguageCode],
                    MissingCategory = (Category)((int)p["category"]),
                    TownMissing = (int?)p["place"]["id"],
                    TownDescription = (string)p["place"]["description"][LanguageCode],
                    Age = (int?)p["age"],
                    DateMissing = (DateTime)p["missingSince"],
                    EyeColor = (string)p["eyes"][LanguageCode],
                    Height = (int?)p["height"],
                    Weight = (int?)p["weight"],
                    BodyType = (string)p["bodyType"][LanguageCode],
                    //Description = (string)p["description"][LanguageCode],
                    Clothes = (string)p["clothes"][LanguageCode],
                    Photo = (string)p["images"]

            }).ToList().Where(a => a.MissingCategory==category).ToList();

            return TempList.ToList<MissingPerson>();
            }
            catch (Exception)
            {
                List<MissingPerson> TempList = new List<MissingPerson>();
                return TempList;
            };

            
        }
        

        public static ReceivedInfo IncomingJson(string json)
        {
            try
            {
            ReceivedInfo tmp = JsonConvert.DeserializeObject<ReceivedInfo>(json);

            String MissingPeopleJson = "";

            int ArraySize = 0;
            int i = 0;
            ArraySize = tmp.missings.Count();

            foreach (object item in tmp.missings)
            {
                i++;
                //if (i==1)
                //{
                //    MissingPeopleJson += "[";
                //}
                MissingPeopleJson += item.ToString();

                if (i!=ArraySize)
                {
                    MissingPeopleJson += ",";
                }
                //else
                //{
                //    MissingPeopleJson += "]";
                //}
            }

            tmp.missingsString = MissingPeopleJson;

             JArray jsonArray = JArray.Parse("["+MissingPeopleJson+"]");

            IList<string> TempList = jsonArray.Select(p => (string)p["images"]).ToList();
                
            tmp.ImagesList = TempList.ToList<string>();

            return tmp;

            }
            catch (Exception)
            {
                ReceivedInfo tmp = new ReceivedInfo();
                return tmp;
            }
        }

